#include "../HAL/INC/led.h"
u32 TimeON=1000,TimeOFF=1000;

int main(void){

	/*initialize led*/
	Init_Led();
	
	while(1){
		Blink_LED(TimeON,TimeOFF);/*time with milliseconds*/
		TimeON*=2;
		TimeOFF*=2;
	}
	
return 0;
}
